/**
 * if else loop that prints i as long that i is below 100
 */
public class IfElse {

    public static void main(String[] args) {


        int i = 10;
        if(i < 100){
            i++;

            System.out.println(i);

        }
        else{
            System.out.println("Glem det makker!");
        }
    }
}
